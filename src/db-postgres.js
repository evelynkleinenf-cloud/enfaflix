async function createPromoCode({
  code,
  title,
  description,
  discountType,
  discountValue,
  minCourses,
  eligibleCourseIds,
  maxRedemptions,
  expiresAt
}) {
  const normalizedCode = String(code || "").trim().toUpperCase();
  await run(
    `
      INSERT INTO promo_codes (
        code,
        title,
        description,
        discount_type,
        discount_value,
        min_courses,
        eligible_course_ids,
        max_redemptions,
        expires_at,
        active,
        created_at
      )
      VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, 1, ?)
    `,
    [
      normalizedCode,
      String(title || normalizedCode).trim(),
      String(description || "").trim(),
      discountType,
      discountValue,
      Math.max(1, Number(minCourses || 1)),
      JSON.stringify(Array.from(new Set((eligibleCourseIds || []).map((value) => Number(value)).filter(Boolean)))),
      maxRedemptions ? Math.max(1, Number(maxRedemptions)) : null,
      expiresAt || null,
      new Date().toISOString()
    ]
  );

  return findPromoCodeByCode(normalizedCode);
}

